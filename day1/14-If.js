function ourTrueorFalse(isItTrue) {
    if(isItTrue) {
        return "Yes, it's true"
    } else {
        return "No, it's false";
    }
}

function trueOrfalse(wasThatTrue) {
    if (wasThatTrue) {
        return "Yes, that was true"
    }
    else {
        return "No, that was false"
    }
}

